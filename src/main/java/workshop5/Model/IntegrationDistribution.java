
package workshop5.Model;

/**
 * Entity containing the data and results of the distribution
 * @author Andy
 */
public class IntegrationDistribution {
    
    private double xValue;
    private int numSeg;
    private int dof;
    private double p;
    private double widthSegment;
    private double errorValue;

    /**
     * @return xValue
     */
    public double getxValue() {
        return xValue;
    }

    /**
     * @param xValue
     */
    public void setxValue(double xValue) {
        this.xValue = xValue;
    }

    /**
     * @return numSeg
     */
    public int getNumSeg() {
        return numSeg;
    }

    /**
     * @param numSeg
     */
    public void setNumSeg(int numSeg) {
        this.numSeg = numSeg;
    }

    /**
     * @return dof
     */
    public int getDof() {
        return dof;
    }

    /**
     * @param dof
     */
    public void setDof(int dof) {
        this.dof = dof;
    }

    /**
     * @return p
     */
    public double getP() {
        return p;
    }

    /**
     * @param p
     */
    public void setP(double p) {
        this.p = p;
    }

    /**
     * @return widthSegment
     */
    public double getWidthSegment() {
        return widthSegment;
    }

    /**
     * @param widthSegment
     */
    public void setWidthSegment(double widthSegment) {
        this.widthSegment = widthSegment;
    }

    /**
     * @return errorValue
     */
    public double getErrorValue() {
        return errorValue;
    }

    /**
     * @param errorValue
     */
    public void setErrorValue(double errorValue) {
        this.errorValue = errorValue;
    }
    
    
    
            
    
}
